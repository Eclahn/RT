#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*ptr;

	ptr = (unsigned char*)b;
	ptr += len;
	while (len--)
		*(--ptr) = (unsigned char)c;
	return (ptr);
}
